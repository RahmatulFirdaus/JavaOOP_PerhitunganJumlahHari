    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {
        if (tfTahun.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Error: isian tahun kosong!", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            int tahun = Integer.parseInt(tfTahun.getText());
            int jumlahHari;
    
            String selectedBulan = cbbxBulan.getSelectedItem().toString();
    
            switch (selectedBulan) {
                case "Februari":
                    if ((tahun % 4 == 0 && !(tahun % 100 == 0)) || (tahun % 400 == 0)) {
                        jumlahHari = 29;
                    } else {
                        jumlahHari = 28;
                    }
                    break;
                case "April":
                case "Juni":
                case "September":
                case "Nopember":
                    jumlahHari = 30;
                    break;
                default:
                    jumlahHari = 31;
                    break;
            }
            lblHasilJumlahHari.setText("Jumlah hari pada bulan " + selectedBulan + " tahun " + tahun + " adalah " + jumlahHari);
        }
    }
    

