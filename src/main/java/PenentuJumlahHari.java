    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            // Buat file
            BufferedWriter out = new BufferedWriter(new FileWriter("jumlahhari.txt"));
    
            // Simpan ke file
            out.write(lblHasilJumlahHari.getText());
    
            // Tampilkan dialog bila berhasil
            JOptionPane.showMessageDialog(null, "Berhasil Disimpan dalam File");
    
            // Tutup output stream
            out.close();
        } catch (IOException e) {
            // Tangkap IOException
            System.err.println("Error: " + e.getMessage());
            e.printStackTrace(); // Menampilkan stack trace untuk membantu melacak masalah
        }
    }
    
    


